
package com.matter.classesandinterfaces;

/*****************************************************************************************
 * Q: Q13: What are elements?
 * A: Elements are substances that cannot be chemically broken down into simpler 
 * substances. So an element is made up of only one kind of atoms. For example, 
 * silver is an element which is made up of only silver atoms. 
 * Elements are the building blocks of all matter.
 * 
 *  
 * @author	Samuel Mayol
 * @company DB Schenker, Inc.
 * @date	07/11/2014
 * @version 1.0
 * 
 *****************************************************************************************/

public class Elements {
	private boolean isMetal;
	private boolean isNon_Metal;
	private boolean isMetalloids;
	private boolean canBeBroken;
	
	/**
	 * @param isMetal
	 * @param isNon_Metal
	 * @param isMetalloids
	 * @param canBeBroken
	 */
	public Elements(boolean isMetal, boolean isNon_Metal, boolean isMetalloids,
			boolean canBeBroken) {
		super();
		this.isMetal = isMetal;
		this.isNon_Metal = isNon_Metal;
		this.isMetalloids = isMetalloids;
		this.canBeBroken = canBeBroken;
	}
	

	
	

}
